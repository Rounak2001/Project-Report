import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";

import Existing from "@/pages/exiting.jsx";
import Index from "@/pages/Index.jsx";
import NotFound from "@/pages/NotFound.jsx";

// --- 1. IMPORT THE RENAMED APP ---
import ProjectReportApp from "@/pages/project-report/ProjectReportApp.jsx";

const queryClient = new QueryClient();

const App = () => {
  return (
    <QueryClientProvider client={queryClient}>
      <TooltipProvider>
        <Toaster />
        <Sonner />
        <BrowserRouter>
          <Routes>
            <Route path="/" element={<ProjectReportApp />} />
            <Route path="/calculator" element={<Index />} />
            <Route path="/existing" element={<Existing />} />

            <Route path="/project-report/*" element={<ProjectReportApp />} />

            <Route path="*" element={<NotFound />} />
          </Routes>
        </BrowserRouter>
      </TooltipProvider>
    </QueryClientProvider>
  );
};

export default App;